    private Boolean checkname() {
    String name = namefiled.getText();
    String query = "SELECT COUNT(*) FROM artist WHERE aname = ?";
    try (Connection connection = DriverManager.getConnection("jdbc:mysql://127.0.0.1:3306/art_management", "root", "manya123");
         PreparedStatement statement = connection.prepareStatement(query)) {
        statement.setString(1, name);
        ResultSet resultSet = statement.executeQuery();
        resultSet.next();
        int count = resultSet.getInt(1);
        if(count == 0){
            return true;
        }
        else{
            return false;
        }
         // Returns true if name doesn't exist, false otherwise
    } catch (SQLException e) {
        e.printStackTrace();
        return false; // Handle any potential SQL exception
    }
}

private Boolean checkdata() {
    // Perform local validation checks
    if (namefiled.getText().equals("") || addressfield.getText().equals("") || passwordField.getText().equals("") || rePasswordField.getText().equals("")) {
        JOptionPane.showMessageDialog(this, "Empty Fields.", "Message", JOptionPane.WARNING_MESSAGE);
        return false;
    }
    if (!passwordField.getText().equals(rePasswordField.getText())) {
        JOptionPane.showMessageDialog(this, "Passwords don't match.", "Message", JOptionPane.WARNING_MESSAGE);
        return false;
    }
    // Validate name against the database
    if (!checkname()) {
        JOptionPane.showMessageDialog(this, "This name is used before.\nChange it.", "Message", JOptionPane.WARNING_MESSAGE);
        return false;
    }
    return true;
}

    
