    private void registerButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_registerButtonActionPerformed
        // TODO add your handling code here:
        if (checkdata()) {
        try (Connection conn = DriverManager.getConnection(DB_URL, DB_USER, DB_PASSWORD)) {
            String sql = "INSERT INTO customer (gid,name, address, password) VALUES (?, ?, ?, ?)";
            String userCust = "INSERT INTO USERS (username,password,role) VALUES (?,?,?)";
            
            try (PreparedStatement stmt = conn.prepareStatement(sql, Statement.RETURN_GENERATED_KEYS)) {
                String cGid = jTextField1.getText();
                String cName = namefiled.getText();
                String cAdd = addressfield.getText();
                String cPass = passwordField.getText();
                
                stmt.setString(1,cGid);
                stmt.setString(2,cName );
                stmt.setString(3,cAdd );
                stmt.setString(4, cPass);
                
                String roleC = "customer";
                
                PreparedStatement statementUc = conn.prepareStatement(userCust);
                statementUc.setString(1,cName);
                statementUc.setString(2,cPass);
                statementUc.setString(3,roleC);
                statementUc.executeUpdate();
                
                int rowsAffected = stmt.executeUpdate();
                if (rowsAffected > 0) {
                    JOptionPane.showMessageDialog(this, "Data Saved.", "Message", JOptionPane.INFORMATION_MESSAGE);
                    LOGINFORM xx = new LOGINFORM();
                    xx.setVisible(true);
                    xx.pack();
                    xx.setDefaultCloseOperation(JFrame.EXIT_ON_CLOSE);
                    this.dispose();
                } else {
                    JOptionPane.showMessageDialog(this, "Failed to save data.", "Error", JOptionPane.ERROR_MESSAGE);
                }
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Database error: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();
        }
    }
    }//GEN-LAST:event_registerButtonActionPerformed

