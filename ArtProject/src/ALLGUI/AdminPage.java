    private void set_home_data()
    {
        try {
            PreparedStatement stmt = connection.prepareStatement("SELECT COUNT(*) FROM artist");
            ResultSet rs = stmt.executeQuery();
            if (rs.next()) {
                totalartsitslabel1.setText(rs.getString(1));
            }
            stmt.close();
            
            stmt = connection.prepareStatement("SELECT COUNT(*) FROM customer");
            rs = stmt.executeQuery();
            if (rs.next()) {
                totalcustomersslabel1.setText(rs.getString(1));
            }
            stmt.close();
            
            stmt = connection.prepareStatement("SELECT COUNT(*) FROM artwork");
            rs = stmt.executeQuery();
            if (rs.next()) {
                totalartworkslabel3.setText(rs.getString(1));
            }
            stmt.close();
            
            stmt = connection.prepareStatement("SELECT COUNT(*) FROM artwork");
            rs = stmt.executeQuery();
            if (rs.next()) {
                totalartboughtphtotolabel3.setText(rs.getString(1));
            }
            stmt.close();
            
            stmt = connection.prepareStatement("SELECT SUM(price) FROM artwork");
            rs = stmt.executeQuery();
            if (rs.next()) {
                totalearnedmoneylabel4.setText(rs.getString(1) + " $");
            }
            stmt.close();
        } catch (SQLException ex) {
            Logger.getLogger(AdminPage.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
    private void set_artist_tbl()
    {
        try {
            DefaultTableModel tblmodel = (DefaultTableModel) artisttbl.getModel();
            tblmodel.setRowCount(0); // Clear existing rows
            
            PreparedStatement stmt = connection.prepareStatement("SELECT * FROM artist");
            ResultSet rs = stmt.executeQuery();
            
            while (rs.next()) {
                int artistId = rs.getInt("artistid");
                String artistName = rs.getString("aname");
                int artistAge = rs.getInt("age");
                String artistNationality = rs.getString("nationality");
                
                PreparedStatement stmt2 = connection.prepareStatement("SELECT COUNT(*) FROM artwork WHERE artid = ?");
                stmt2.setInt(1, artistId);
                ResultSet rs2 = stmt2.executeQuery();
                int totalArtwork = 0;
                if (rs2.next()) {
                    totalArtwork = rs2.getInt(1);
                }
                stmt2.close();
                
                stmt2 = connection.prepareStatement("SELECT COUNT(*) FROM artwork WHERE artid = ? ");
                stmt2.setInt(1, artistId);
                rs2 = stmt2.executeQuery();
                int totalBoughtPhotos = 0;
                if (rs2.next()) {
                    totalBoughtPhotos = rs2.getInt(1);
                }
                stmt2.close();
                
                stmt2 = connection.prepareStatement("SELECT SUM(price) FROM artwork WHERE artid = ? ");
                stmt2.setInt(1, artistId);
                rs2 = stmt2.executeQuery();
                int totalEarnedMoney = 0;
                if (rs2.next()) {
                    totalEarnedMoney = rs2.getInt(1);
                }
                stmt2.close();
                
                tblmodel.addRow(new Object[]{artistId, artistName, artistAge, artistNationality, totalArtwork, totalBoughtPhotos, totalEarnedMoney});
            }
            
            stmt.close();
        } catch (SQLException ex) {
            Logger.getLogger(AdminPage.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }
    
 
    
    private void set_customer_tbl() {
        try {
        DefaultTableModel tblmodel = (DefaultTableModel) customertbl.getModel();
        tblmodel.setRowCount(0); // Clear existing rows
        
        PreparedStatement stmt = connection.prepareStatement("SELECT * FROM customer");
        ResultSet rs = stmt.executeQuery();
        
        while (rs.next()) {
            int customerId = rs.getInt("custid"); // Assuming custid is the correct column name for customer id
            String customerName = rs.getString("name"); // Corrected column name
            String customerAddress = rs.getString("address"); // Corrected column name
            
            PreparedStatement stmt2 = connection.prepareStatement("SELECT COUNT(*) FROM buys_artwork WHERE custid = ?");
            stmt2.setInt(1, customerId);
            ResultSet rs2 = stmt2.executeQuery();
            int totalBoughtPhotos = 0;
            if (rs2.next()) {
                totalBoughtPhotos = rs2.getInt(1);
            }
            stmt2.close();
            
            tblmodel.addRow(new Object[]{customerId, customerName, customerAddress, totalBoughtPhotos});
        }
        
        stmt.close();
    } catch (SQLException ex) {
        Logger.getLogger(AdminPage.class.getName()).log(Level.SEVERE, null, ex);
    }
    }
    
    
    
