    private int calculateTotalSoldPhotos() {
        int totalSoldPhotos = 0;

        try (Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/art_management", "root", "manya123")) {
            String query = "SELECT COUNT(*) FROM buys_artwork";
            try (PreparedStatement stmt = conn.prepareStatement(query)) {
                ResultSet rs = stmt.executeQuery();
                if (rs.next()) {
                    totalSoldPhotos = rs.getInt(1);
                }
            }
        } catch (SQLException ex) {
            ex.printStackTrace();
        }

        return totalSoldPhotos;
    }
    
    
